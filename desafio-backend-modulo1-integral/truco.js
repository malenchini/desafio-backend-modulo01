function solucao(carta) {
    if (carta === "Q") {
        return console.log("J")
    } else if (carta === "J") {
        return console.log("K")
    } else if (carta === "K") {
        return console.log("A")
    } else if (carta === "A") {
        return console.log("2")
    } else if (carta === "2") {
        return console.log("3")
    } else if (carta === "3") {
        return console.log("Q")
    }
}